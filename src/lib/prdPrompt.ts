export const PRD_PROMPT = `지금 논의/작업 중인 주제(불명확하면 무엇에 대한 PRD인지 먼저 물어봐)에 대해 **누락 없는 깊이 있는 PRD**를 작성해줘. 캐주얼 요약 말고 아래 절차·구조를 지켜.

[절차]
1) **질문 먼저** — 모호하거나 결정이 필요한 부분은 가정하지 말고 나에게 먼저 질문해(범위·타겟·제약·우선순위·성공기준).
2) **깊이 조사** — 관련 코드/기존 구현/데이터 모델을 읽고, 필요하면 최신 베스트프랙티스를 웹에서 조사해 근거로 삼아.
3) PRD를 작성해 **\`PRD.md\`**(또는 주제명 .md)에 저장.
4) **완결성 자기점검** — 작성 후 "빠진 게 뭔가?"를 스스로 검토(엣지케이스·실패/에러 경로·보안·권한·마이그레이션·접근성·비기능요구·미해결 질문). 빠진 건 채우거나 'Open Questions'에 명시.
5) 핵심을 요약 보고하고 **내 검토·승인**을 기다려(구현은 승인 후).

[구조 — 모든 섹션 채울 것]
- **배경/문제 정의**: 왜 필요한가, 현재 무엇이 문제인가
- **목표 & 비목표(Non-goals)**: 이번에 하는 것 / 명시적으로 안 하는 것
- **유저스토리 / 페르소나**: "누가 / 무엇을 / 왜"
- **기능 요구사항**: 항목별로 구체적으로
- **비기능 요구사항**: 성능·보안·접근성(WCAG)·확장성·관측성
- **수용기준(실행 가능)**: "잘 동작한다" ❌ → "이 명령/테스트/시나리오가 통과한다" ✅ 로 검증 가능하게
- **엣지케이스 & 에러 처리**: 빈 상태/실패/경계/동시성/권한
- **데이터 모델 / API 영향**: 스키마·계약·마이그레이션
- **의존성 & 리스크**: 외부 연동, 불확실성(→ 리스크 스파이크 제안)
- **마일스톤 / 수직 슬라이스**: 단계별 분해 + 각 DoD
- **측정 지표(성공 지표)**: 무엇으로 성공을 판단
- **범위 밖(Out of scope)** & **Open Questions(미해결 질문)**

핵심 가치: **가정하지 말고 질문**, **검증 가능한 수용기준**, **빠진 것 없는 완결성**.`;
